#!/usr/bin/env python3

# 运算符可以分为：算术运算符、比较运算符和逻辑运算符。优先级是：算术运算符>比较运算符>逻辑运算符。不过呢，开始没背下来优先级，最好使用括号。这样不用背，也增加了代码的可读性。

print( 5 / 2 ) # 2.5 
print( 5 // 2) # 丢弃余数，只保留商
print( 5 % 2 ) # 求余数
print( 5 ** 3) # 5的三次方
print( 5 > 3 ) # 返回Ture
print( 3 > 5 ) # 返回False
print( 20 > 10 > 5)  # python支持连续比较
print( 20 > 10 and 10 > 5 ) # 与上面的含义相同
print( not 20 > 10) # False

# 注意：True和False是关键字，区分大小写





